package day8;

public class CharATString {
    public static void main(String[] args){
        	String s = ("Welcome");
		
		System.out.println(s.charAt(3)); // It Prints the index of the character
		
		System.out.println(s.contains("Wel"));
		System.out.println(s.contains("wel"));
		System.out.println(s.contains("WEL"));
    }
}
